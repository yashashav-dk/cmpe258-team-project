from pathlib import Path

from benchmark.manifest import BenchmarkCase


class InjectionError(Exception):
    pass


def _assert_target_in_scope(workspace_dir: Path, target_file: str, allowed_paths: list) -> Path:
    target = (workspace_dir / target_file).resolve()
    root = workspace_dir.resolve()
    if not str(target).startswith(str(root)):
        raise InjectionError(f"Target escapes workspace scope: {target_file}")

    if allowed_paths:
        allowed = [(workspace_dir / path).resolve() for path in allowed_paths]
        if not any(str(target).startswith(str(item)) for item in allowed):
            raise InjectionError(f"Target {target_file} outside allowed_paths")

    if not target.exists():
        raise InjectionError(f"Target file does not exist: {target}")
    return target


def apply_injection(case: BenchmarkCase, workspace_dir: str) -> None:
    """
    Apply deterministic injection to a workspace target file.

    Supports two modes:
      - metadata.injection_mode == "replace": exact old/new replacement
      - fallback: parse synthetic patch payload generated by SyntheticMutationAdapter
    """
    workdir = Path(workspace_dir)
    target = _assert_target_in_scope(workdir, case.target_file, case.allowed_paths)
    mode = case.metadata.get("injection_mode", "patch")

    original = target.read_text(encoding="utf-8")
    mutated = original

    if mode == "replace":
        old = case.metadata.get("old_content")
        new = case.metadata.get("new_content")
        if not isinstance(old, str) or not isinstance(new, str):
            raise InjectionError("replace mode requires metadata old_content/new_content")
        if old not in original:
            # Idempotency guard: allow re-running the same manifest if this
            # case has already been injected in the workspace.
            if new in original:
                return
            raise InjectionError("old_content not found in target file")
        mutated = original.replace(old, new, 1)
    else:
        marker = "@@\n-"
        if marker not in case.injection_patch or "\n+" not in case.injection_patch:
            raise InjectionError("Unsupported patch payload format")
        body = case.injection_patch.split(marker, 1)[1]
        minus, plus = body.split("\n+", 1)
        old = minus
        new = plus
        if old not in original:
            # Idempotency guard for patch-mode synthetic cases.
            if new in original:
                return
            raise InjectionError("Patch old payload not found in target file")
        mutated = original.replace(old, new, 1)

    if mutated == original:
        raise InjectionError("Injection made no file changes")
    target.write_text(mutated, encoding="utf-8")
